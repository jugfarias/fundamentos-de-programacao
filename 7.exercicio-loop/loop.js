// imprimir n√∫meros de 1 a 10 usando loops

// for
console.log("For:");
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// while
console.log("While:");  
let i = 1;
while (i <= 10) {
    console.log(i);
    i++;
}

// do-while
console.log("Do-While:");
i = 1;
do {
    console.log(i);
    i++;
} while (i <= 10);