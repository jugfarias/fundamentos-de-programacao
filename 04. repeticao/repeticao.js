// printar os nÃºmeros de 0 a 4 no console com for, while e do-while

// for
console.log("for:");
for (let i = 5; i > 0; i--) {
    console.log(i);
}

// while
console.log("while:");
let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}

// do-while
console.log("do-while:");
i = 0;
do {
    console.log(i);
    i++;
} while (i < 5);