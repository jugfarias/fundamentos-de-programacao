// variável armazenando uma string
let nome;           // declaração da variável
nome = "Jupiter";   // atribuição de valor

// variável armazenando um número
let idade;          // declaração da variável
idade = 27;         // atribuição de valor
let a, b, c;        // declaração de múltiplas variáveis
a = 1;              // atribuição de valor
b = 2;              // atribuição de valor
c = 3;              // atribuição de valor
soma = a + b + c;   // soma dos valores

// variável armazenando um booleano
let chuva;          // declaração da variável
chuva = true;       // atribuição de valor

// print das variáveis no console
console.log(nome);  // Jupiter
console.log(idade); // 27
console.log(soma);  // 6
console.log(chuva); // true