// no JS vetor = array

// criação de um vetor vazio
let vetor = []; 

// adicionar elementos ao final do vetor
vetor.push(10); // adiciona o elemento 10 no vetor

// mostrar o vetor no console
console.log(vetor);

// adicionar elemento ao início do vetor
vetor.unshift(20); // adiciona o elemento 20 no início do vetor

// remover e retornar o último elemento do vetor
let x = vetor.pop(); // remove o último elemento do vetor e armazena em x

// remover e retornar o primeiro elemento do vetor
let y = vetor.shift(); // remove o primeiro elemento do vetor e armazena em y

// printa x e y
console.log(x, y)